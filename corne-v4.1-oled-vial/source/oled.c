// VERTICAL ORIENTATION
oled_rotation_t oled_init_user(oled_rotation_t rotation) {
    if (!is_keyboard_master()) {
        return OLED_ROTATION_180;
    }
    return OLED_ROTATION_270;
}

void render_status(void) {
    return;
}

// MAIN OLED TASK LOOP
bool oled_task_user(void) {
    if (is_keyboard_master()) {
        oled_clear();
        
        //LINE 0: SLAB 
        oled_set_cursor(0, 0);
        uint8_t layer = get_highest_layer(layer_state | default_layer_state);
        
        oled_write_P(PSTR("  "), false);
        if (layer < 10) {
            oled_write_char('0' + layer, false);
        } else {
            oled_write_char('?', false);
        }
        oled_write_P(PSTR("  "), false);
        
        // LINE 1: EMPTY
        
        // LINE 2: MOD
        oled_set_cursor(0, 2);
        uint8_t mods = get_mods() | get_oneshot_mods();
        
        if (host_keyboard_led_state().caps_lock) {
            oled_write_P(PSTR("CAPS"), false);
        } else if (mods & MOD_MASK_SHIFT) {
            oled_write_P(PSTR("SHFT"), false);
        } else if (mods & MOD_MASK_CTRL) {
            oled_write_P(PSTR("CTRL"), false);
        } else if (mods & MOD_MASK_ALT) {
            oled_write_P(PSTR("ALT "), false);
        } else if (mods & MOD_MASK_GUI) {
            oled_write_P(PSTR("GUI "), false);
        } else {
            oled_write_P(PSTR("----"), false);
        }
        
        // LINE 3: EMPTY
        
        // PERSONNEL CHANGE
        uint32_t now = timer_read32();
        if (timer_elapsed32(banana_timer) > BANANA_FRAME_DELAY) {
            banana_timer = now;
            current_frame = (current_frame + 1) % BANANA_FRAMES;
        }
        
        //  BANANA OUTPUT (LINES 4-7)
        oled_set_cursor(0, 4);
        oled_write_raw_P((const char*)banana_frames[current_frame], FRAME_SIZE);
        
        oled_render();
    } else {
        oled_off();
    }
    return false;
}

